
package model;

public class Car {
    
    private String id;
    private String name;
    private String type;
    private String size;
    private String color;
    private int price;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
    
    /**
     * @return the name
     */
    public String gettype() {
        return type;
    }

    /**
     * @param name the name to set
     */
    public void settype(String type) {
        this.type = type;
    }
    
    /**
     * @return the name
     */
    public String getsize() {
        return size;
    }

    /**
     * @param name the name to set
     */
    public void setsize(String size) {
        this.size = size;
    }
    
    /**
     * @return the name
     */
    public String getcolor() {
        return color;
    }

    /**
     * @param name the name to set
     */
    public void setcolor(String color) {
        this.color = color;
    }

    /**
     * @return the price
     */
    public int getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(int price) {
        this.price = price;
    }
    
}